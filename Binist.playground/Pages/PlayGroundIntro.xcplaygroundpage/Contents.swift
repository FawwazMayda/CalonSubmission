 # CoreML / Machine Learning
 
 [Core ML](https://developer.apple.com/machine-learning/) is Apple Framework to utilizing Machine Learning in Apps for Apple's Platform. Core ML provided many common Machine Learning case ranging from Hand Writtern Digit Recognition (this is what I am using) until a complex ones like BERT-SQuAD for QA purposes. The Hand Written Digit Recognition is trained on MNIST Dataset for the purpose. You can find some of Machine Learning model [here](https://developer.apple.com/machine-learning/models/)
 
 # What is MNIST Dataset?
 Well, it's a huge databse of **28x28 pixel images** of handwritten digits (0-9). In the area of machine learning it's the most famous database since **1999** when it was created by **[Yann LeCun](http://yann.lecun.com/)**, **Corinna Cortes** and **[Christopher J. C. Burges](http://chrisburges.net)**. It has 60,000 grayscale images under the training set and 10,000 grayscale images under the test set.
